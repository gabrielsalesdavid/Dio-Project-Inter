
public class ResoluçãoExercicio {

    public static void main(String[] args) {

        int a = 1;
        int b = 10;

        float c = 4.5f;
        float d = 5.5f;

        double e = 3.14;

        char f = 'A';
        char g = 'B';

        String h = "Olá, Mundo!";
        String i = "Java é incrível!";

        boolean j = true;
        boolean k = false;

        long l = 1000000000L;
        long m = 2000000000L;

        short n = 1000;
        short o = 2000;

        byte p = 100;
        byte q = 50;

        System.out.println("a == b: " + (a == b));
        System.out.println("c > d: " + (c > d));
        System.out.println("e <= 3.14: " + (e <= 3.14));
        System.out.println("f != g: " + (f != g));
        System.out.println("h.equals(i): " + h.equals(i));
        System.out.println("j && k: " + (j && k));
        System.out.println("l > m: " + (l > m));
        System.out.println("n < o: " + (n < o));
        System.out.println("p >= q: " + (p >= q));
    }
}
